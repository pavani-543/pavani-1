class Relational
{
 public static void main(String args[])
 {
  int a=45,b=67;
  //greater than(>)
  System.out.println(a>b);//false
  //greater than or equal to(>=)
  System.out.println(a>=b);//false
  //less than(<)
  System.out.println(a<b);//true
  //less than or equal to(<=)
  System.out.println(a<=b);//true
  //eualto(==)
  System.out.println(a==b);//false
  //not equal to(!=)
  System.out.println(a!=b);//true
  }
}
  
