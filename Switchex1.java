class Switchex1
{
 public static void main (String args[])
 {
  char clr='G';
  switch(clr)
  {
   case 'b':System.out.println("blue");
            break;
   case 'p':System.out.println("pink");
            break;
   case 'y':System.out.println("yellow");
            break;
   case 'g':System.out.println("green");
             break;
            default:
             System.out.println("invalid entry");
    }
  }
}