class Idex
{
 public static void main(String args[])
 {
  // pre incrementation(++x)
  int x=78;
  System.out.println(x);//78
  System.out.println(++x);//78+1=79 
  System.out.println(x);//79
  //pre increment(x++)
  int y=90;
  System.out.println(y);//90
  System.out.println(y++);//90
  System.out.println(y);//90+1=91
  //pre decrementation(--x)
  int z=12;
  System.out.println(z);//12
  System.out.println(--z);//12-1=11
  System.out.println(z);//11
  //post decrementation(x--)
  int c=34;
  System.out.println(c);//34
  System.out.println(c);//34
  System.out.println(c);//34-1-33
 }
}



































